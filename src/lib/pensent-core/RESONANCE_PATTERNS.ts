export function findResonantAdapters(adapterName: string): string[] {
  const resonant: string[] = [];
  
  // Check high resonance pairs
  RESONANCE_PATTERNS.highResonance.forEach(({ pair, score }) => {
    if (pair.includes(adapterName)) {
      resonant.push(...pair.filter(a => a !== adapterName));
    }
  });
  
  // Check medium resonance triples
  RESONANCE_PATTERNS.mediumResonance.forEach(({ triple, score }) => {
    if (triple.includes(adapterName)) {
      resonant.push(...triple.filter(a => a !== adapterName));
    }
  });
  
  return [...new Set(resonant)];
}

export function calculateResonanceScore(adapter1: string, adapter2: string): number {
  // Check high resonance
  const high = RESONANCE_PATTERNS.highResonance.find(
    ({ pair }) => pair.includes(adapter1) && pair.includes(adapter2)
  );
  if (high) return high.score;
  
  // Check if they're in same emergent quadrant
  const quadrant = RESONANCE_PATTERNS.emergentQuadrants.find(
    ({ adapters }) => adapters.includes(adapter1) && adapters.includes(adapter2)
  );
  if (quadrant) return 0.75;
  
  // Check cross-domain bridges
  const bridge = RESONANCE_PATTERNS.crossDomainBridges.find(
    ({ from, to }) => 
      (from === adapter1 && to === adapter2) || 
      (from === adapter2 && to === adapter1)
  );
  if (bridge) return 0.65;
  
  return 0.3; // Base resonance
}

